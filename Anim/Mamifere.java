package Anim;

public class Mamifere {
	private int nb_glande;
	private int qtté_poils;
	
	public Mamifere(int nb_glande, int qtté_poils) {
		this.nb_glande = nb_glande;
		this.qtté_poils = qtté_poils;
		
		
	}

	public int getNb_glande() {
		return nb_glande;
	}

	public void setNb_glande(int nb_glande) {
		this.nb_glande = nb_glande;
	}

	public int getQtté_poils() {
		return qtté_poils;
	}

	public void setQtté_poils(int qtté_poils) {
		this.qtté_poils = qtté_poils;
	}
	
	
}
